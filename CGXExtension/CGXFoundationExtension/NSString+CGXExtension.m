#pragma mark - 验证比较

- (BOOL)isEmpty {
    if (!self.length || ![[self stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceCharacterSet]] length]) {
        return YES;
    }
    return NO;
}

- (BOOL)isEmail {
    return [self isMatch:RX(@"[A-Z0-9a-z._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,4}")];
}

- (BOOL)isPhone {
    return [self isMatch:RX(@"^(0|86|17951)?(13|14|15|17|18)[0-9]{9}$")];
}

- (BOOL)isExpressNum {
    return [self isMatch:RX(@"[A-Za-z0-9]{1,20}")];
}

- (BOOL)isBarcode {
    return [self isMatch:RX(@"[0-9]{10}")];
}

- (BOOL)isChinese {
    return [self isMatch:RX(@"(^[\u4e00-\u9fa5]+$)")];
}

#pragma mark - 加密处理

static NSString * const kCharactersToBeEscapedInQueryString = @"?!@#$^&%*+,:;='\"`<>()[]{}/\\| ";

- (NSString *)URLDecode {
    return [self stringByRemovingPercentEncoding];
}

- (NSString *)URLEncode {
    NSCharacterSet *allowedCharacters = [[NSCharacterSet characterSetWithCharactersInString:kCharactersToBeEscapedInQueryString] invertedSet];
    return [self stringByAddingPercentEncodingWithAllowedCharacters:allowedCharacters];
}

- (NSString *)base64String {
    return [[self dataUsingEncoding:NSASCIIStringEncoding] base64EncodedStringWithOptions:0];
}

- (NSString *)base64StringToOriginString {
    return [[NSString alloc] initWithData:[[NSData alloc] initWithBase64EncodedString:self options:0] encoding:NSASCIIStringEncoding];
}

- (NSString *)md5 {
    const char *cString = [self UTF8String];
    uint8_t digest[16];
    CC_MD5(cString, (CC_LONG)strlen(cString), digest);
    NSMutableString *output = [NSMutableString stringWithCapacity:CC_MD5_DIGEST_LENGTH * 2];
    for (int i = 0; i < CC_MD5_DIGEST_LENGTH; i++) {
        [output appendFormat:@"%02x", digest[i]];
    }
    return output;
}

- (NSString *)hmacsha1WithSecretKey:(NSString *)secretKey {
    if (!self.length) {
        return @"";
    }
    const char *cKey  = [secretKey cStringUsingEncoding:NSASCIIStringEncoding];
    const char *cData = [self cStringUsingEncoding:NSASCIIStringEncoding];
    unsigned char cHMAC[CC_SHA1_DIGEST_LENGTH];
    CCHmac(kCCHmacAlgSHA1, cKey, strlen(cKey), cData, strlen(cData), cHMAC);
    NSData *HMAC = [[NSData alloc] initWithBytes:cHMAC length:sizeof(cHMAC)];
    return [HMAC base64EncodedStringWithOptions:NSDataBase64Encoding64CharacterLineLength];
}

#pragma mark - Other

- (CGSize)calculateSize:(CGSize)size font:(UIFont *)font {
    CGSize expectedLabelSize = CGSizeZero;
    
    NSMutableParagraphStyle *paragraphStyle = [[NSMutableParagraphStyle alloc] init];
    paragraphStyle.lineBreakMode = NSLineBreakByWordWrapping;
    NSDictionary *attributes = @{NSFontAttributeName:font, NSParagraphStyleAttributeName:paragraphStyle.copy};
    
    expectedLabelSize = [self boundingRectWithSize:size options:NSStringDrawingUsesLineFragmentOrigin attributes:attributes context:nil].size;
    
    return CGSizeMake(ceil(expectedLabelSize.width), ceil(expectedLabelSize.height));
}

